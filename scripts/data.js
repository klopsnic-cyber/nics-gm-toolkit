/** Würfelt n W-`sides` und summiert. */
export function roll(n, sides) {
  let sum = 0;
  for (let i = 0; i < n; i++) sum += Math.floor(Math.random() * sides) + 1;
  return sum;
}

/** Zufälliges Element aus einem Array. */
export function pick(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

/** Zufälliger Schlüssel gemäß Gewichtstabelle {key: weight}. */
export function pickWeighted(weights) {
  const total = Object.values(weights).reduce((a, b) => a + b, 0);
  let r = Math.random() * total;
  for (const [key, w] of Object.entries(weights)) {
    r -= w;
    if (r <= 0) return key;
  }
  return Object.keys(weights)[0];
}
